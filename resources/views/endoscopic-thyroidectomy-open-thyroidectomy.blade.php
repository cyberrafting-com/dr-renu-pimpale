                        <p>Thyroid surgery, whether endoscopic or open, is typically recommended for conditions such as thyroid nodules, thyroid cancer, or hyperthyroidism that does not respond to other treatments. It aims to remove part or all of the thyroid gland to alleviate symptoms and/or treat underlying conditions.</p>
